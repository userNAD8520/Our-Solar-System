// !Functions for the Sun

// Adds styling to the sun's info and image elements
function myFunction_addSun() {
  var element = document.getElementById("sun_info");
  element.classList.add("styling"); // Add "styling" class to the sun's information element
  var element = document.getElementById("img1");
  element.classList.add("img-style"); // Add "img-style" class to the sun's image element
}

// Removes styling from the sun's info and image elements
function myFunction_removeSun() {
  var element = document.getElementById("sun_info");
  element.classList.remove("styling"); // Remove "styling" class from the sun's information element
  var element = document.getElementById("img1");
  element.classList.remove("img-style"); // Remove "img-style" class from the sun's image element
}

